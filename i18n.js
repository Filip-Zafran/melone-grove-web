// ============================================================================
// Language Management
// ============================================================================

class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('meloneGrove_language') || 'de';
        this.init();
    }

    init() {
        // Set language on page load
        this.setLanguage(this.currentLang);

        // Set up language toggle button
        this.setupToggleButton();
    }

    setLanguage(lang) {
        if (!translations[lang]) {
            console.warn(`Language ${lang} not found. Using 'de' as default.`);
            lang = 'de';
        }

        this.currentLang = lang;
        localStorage.setItem('meloneGrove_language', lang);

        // Translate all elements with data-i18n attribute
        this.translatePage();

        // Update toggle button
        this.updateToggleButton();

        // Update HTML lang attribute
        document.documentElement.lang = lang;
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = translations[this.currentLang][key];

            if (translation) {
                // Check if it's a form label or other special case
                if (element.tagName === 'LABEL') {
                    // For labels with links, preserve the link HTML
                    if (element.innerHTML.includes('<a')) {
                        const linkMatch = element.innerHTML.match(/<a[^>]*>.*?<\/a>/);
                        if (linkMatch) {
                            element.innerHTML = translation.replace('Datenschutzerklärung', '') + linkMatch[0];
                        } else {
                            element.textContent = translation;
                        }
                    } else {
                        element.textContent = translation;
                    }
                } else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                } else {
                    element.textContent = translation;
                }
            } else {
                console.warn(`Translation key not found: ${key}`);
            }
        });
    }

    setupToggleButton() {
        const toggleBtn = document.getElementById('langToggle');
        if (toggleBtn) {
            toggleBtn.addEventListener('click', () => this.toggleLanguage());
        }
    }

    toggleLanguage() {
        const newLang = this.currentLang === 'de' ? 'en' : 'de';
        this.setLanguage(newLang);
    }

    updateToggleButton() {
        const toggleBtn = document.getElementById('langToggle');
        if (!toggleBtn) return;

        const flagSpan = toggleBtn.querySelector('.lang-flag');
        const textSpan = toggleBtn.querySelector('.lang-text');

        if (this.currentLang === 'de') {
            flagSpan.textContent = '🇩🇪';
            textSpan.textContent = 'DE';
            toggleBtn.title = 'Switch to English';
        } else {
            flagSpan.textContent = '🇬🇧';
            textSpan.textContent = 'EN';
            toggleBtn.title = 'Zu Deutsch wechseln';
        }
    }

    getText(key) {
        return translations[this.currentLang][key] || key;
    }
}

// Initialize language manager when DOM is ready
let languageManager;
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function() {
        languageManager = new LanguageManager();
    });
} else {
    languageManager = new LanguageManager();
}
